package springboot;

import java.awt.image.BufferedImage;

public class Image {
	BufferedImage im;
	String id;
	public BufferedImage getIm() {
		return im;
	}
	public void setIm(BufferedImage im) {
		this.im = im;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
}
